public class TablaMultiplicacion {
  public static void main(String[] args) {
    
    for(int contador = 1;contador <=7;contador++) {
      for (int multiplicacion = 1; multiplicacion <= 3;multiplicacion++) {
        System.out.print(multiplicacion * contador);
        System.out.print(" ");
      }

      System.out.println();
      
    }
    
  }
}
